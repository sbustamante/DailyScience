from scipy import misc
import matplotlib.pyplot as plt
import os
import sys

#==================================================================================================
#			PARAMETERS
#==================================================================================================
image_name = sys.argv[1]

#==================================================================================================
#			CODE
#==================================================================================================
#Loading picture
picture = misc.imread(image_name)
fig = plt.figure( figsize=(10,7) )
fig.subplots_adjust( bottom=0.0, left=0.0, top=1.0, right=1.0)
plt.imshow( picture )
plt.axis( 'off' )
plt.text( 15,158,"todo",bbox=dict(facecolor=(0.99,0.84,0.58),lw=0), fontsize=55 )
plt.text( 350,105,"OO",color=(0.99,0.84,0.58), bbox=dict(facecolor=(0.99,0.84,0.58),lw=0), fontsize=55 )
